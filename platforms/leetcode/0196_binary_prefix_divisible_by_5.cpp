#include "../../template.hpp"

vi sol(vi nums) {
    int n = nums.size();
    vi ans(n);
    ull num = 0;
    for (int i = 0; i < n; ++i) {
        num = num * 2 + nums[i];
        num %= 5;
        ans[i] = num == 0;
    }
    return ans;
}

